    private void MasukanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MasukanActionPerformed
        boolean bisa= true;
        for (int i = 0; i < tabelpenjualan.getRowCount(); i++) {
            if(Integer.parseInt(tabelpenjualan.getValueAt(i, 0).toString())==Integer.parseInt(tabelbarang.getValueAt(tabelbarang.getSelectedRow(), 0).toString())){
                bisa = false;
                i = tabelpenjualan.getRowCount();
            } else bisa = true;
        }
        if(tcustomer.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Belum Memilih Customer");
        }else if(!bisa){
            JOptionPane.showMessageDialog(this, "Barang Sudah Ada");
        }else if(tabelbarang.isRowSelected(tabelbarang.getSelectedRow())){
            
        Vector<Object> row= new Vector<Object>();
        row.add(tabelbarang.getValueAt(tabelbarang.getSelectedRow(), 0));
        row.add(tabelbarang.getValueAt(tabelbarang.getSelectedRow(), 1));
        row.add(tabelbarang.getValueAt(tabelbarang.getSelectedRow(), 2));
        String input = JOptionPane.showInputDialog("Masukan Jumlah :");
        int jumlah=0;
        
        if(input!=null){
            try{
            jumlah = Integer.parseInt(input);
            } catch(Exception ex){
                JOptionPane.showMessageDialog(this, "Masukan Jumlah Dengan Benar");
            }
        }
        row.add(jumlah);
        String harga = JOptionPane.showInputDialog(this, "Masukan Harga Barang :");
        int hargaa=0;
        if(harga!=null){
            try{
            hargaa = Integer.parseInt(harga);
            } catch(Exception ex){
                JOptionPane.showMessageDialog(this, "Masukan Harga dengan Benar");
            }
        }
        row.add(hargaa);
        row.add(jumlah*hargaa);
        vtabelpenjualan.add(row);   
        Vector<String> tableHeader = new Vector<String>();
        tableHeader.add("Id Barang");
        tableHeader.add("Article");
        tableHeader.add("Deskripsi Barang");
        tableHeader.add("Pcs");
        tableHeader.add("Harga");
        tableHeader.add("Jumlah");
        Vector tableData = vtabelpenjualan;
        if(tableData!=null){
            tabelpenjualan.setModel(new DefaultTableModel(tableData,tableHeader));
            DefaultTableCellRenderer renderer=new DefaultTableCellRenderer();
            renderer.setHorizontalAlignment(JLabel.RIGHT);
            tabelpenjualan.getColumnModel().getColumn(2).setMinWidth(200);
        } 
        }
        
    }//GEN-LAST:event_MasukanActionPerformed

