    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        if(tabelcustomer.isRowSelected(tabelcustomer.getSelectedRow())){
            inputcustomer.setVisible(true);
            inputcustomer.tipe(tabelcustomer.getValueAt(tabelcustomer.getSelectedRow(), 0).toString());
        } else JOptionPane.showMessageDialog(this, "Belum Pilih Customer");
    }//GEN-LAST:event_editActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        if(JOptionPane.showConfirmDialog(this, "Hapus Data "+tabelcustomer.getValueAt(tabelcustomer.getSelectedRow(), 1).toString())==0&&customer.delete(Integer.parseInt(tabelcustomer.getValueAt(tabelcustomer.getSelectedRow(), 0).toString()))){
            JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus");
            tampilkanDataAll();
        }else JOptionPane.showMessageDialog(this, "Data Gagal Dihapus");
    }//GEN-LAST:event_hapusActionPerformed

