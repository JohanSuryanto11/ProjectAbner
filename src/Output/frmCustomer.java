    public boolean tampilkanDataAll(){
     Vector<String> tableHeader = new Vector<String>();
        tableHeader.add("Id Customer");
        tableHeader.add("Nama");
        tableHeader.add("Alamat");
        tableHeader.add("Kota");
        tableHeader.add("Telp");
        tableHeader.add("Provinsi ");
        Vector tableData = customer.Load();
        if(tableData!=null){
            tabelcustomer.setModel(new DefaultTableModel(tableData,tableHeader));
            DefaultTableCellRenderer renderer=new DefaultTableCellRenderer();
            renderer.setHorizontalAlignment(JLabel.RIGHT);
            tabelcustomer.getColumnModel().getColumn(2).setMinWidth(200);
            return true;
        } else return false;
    }
