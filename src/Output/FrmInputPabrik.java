    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try{
        Integer.parseInt(txtIdSales.getText());
        
        tbp.setIdPabrik(Integer.parseInt(txtIdSales.getText()));
        tbp.setNamaPabrik(txtNama.getText());
        tbp.setAlamat(txtAlamat.getText());
        tbp.setNoTelepon(txtNoTelepon.getText());

        if (editMode) {
            if (tbp.update(Integer.parseInt(txtIdSales.getText())) == true) {
                JOptionPane.showMessageDialog(this, "Data telah di Update");
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal di Update");
            }
        } else if (tbp.validasiId(Integer.parseInt(txtIdSales.getText())) <= 0) {
            if (tbp.insert()) {
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal disimpan");
            }
        } else {
            JOptionPane.showMessageDialog(this, "IdPabrik sudah terdaftar");
        }
        
        dispose();
        }catch(Exception ex){
     JOptionPane.showMessageDialog(this, "Masukan Id dengan Angka");
                }
    }//GEN-LAST:event_btnSimpanActionPerformed

