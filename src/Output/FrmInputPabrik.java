    public void execute(Integer IdPabrik){
        editMode = (!IdPabrik.equals(""));
        if(!IdPabrik.equals("")){
            if(tbp.select(IdPabrik)){
            txtIdSales.setText(String.valueOf(tbp.getIdPabrik()));
            txtIdSales.setEnabled(false);
            txtNama.setText(tbp.getNamaPabrik());            
            txtAlamat.setText(String.valueOf(tbp.getAlamat()));
            txtNoTelepon.setText(String.valueOf(tbp.getNoTelepon()));
        }
            
        }else{
            txtIdSales.setEnabled(true);
           this.setVisible(true);
       }
    }
    
